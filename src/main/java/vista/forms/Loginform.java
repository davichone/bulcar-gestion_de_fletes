    private void btnLoginBotonLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginBotonLoginActionPerformed

        if (btnLogin.getText().equals("Reintentar")) {
            // Si falló antes, este botón ahora sirve para volver a chequear
            btnLogin.setText("Ingresar");
            verificarConexionAlIniciar();
        } else {
            // Aquí va tu lógica normal de login (validar usuario y clave)
            // ejecutarLogin(); // Tu lógica de validar usuario en la BD
            String username = txtUser.getText().trim();
            String password = new String(txtJpassword.getPassword()); // Recuerda lo que hablamos del JPasswordField

            if (username.isEmpty() || password.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Por favor, completa ambos campos.");
                return;
            }

            // Instanciamos el controlador
            UserController userControl = new UserController();
            User usuarioLogueado = userControl.validateLogin(username, password);

            if (usuarioLogueado != null) {
                JOptionPane.showMessageDialog(this, "¡Bienvenido " + usuarioLogueado.getFullName() + "!");

                // Pasamos el usuario a la ventana principal
                InicialForm principal = new InicialForm(usuarioLogueado);
                principal.setVisible(true);
                this.dispose(); // Cierra el login
            } else {
                JOptionPane.showMessageDialog(this, "Credenciales incorrectas o usuario inactivo.", "Error", JOptionPane.ERROR_MESSAGE);
                txtJpassword.setText(""); // Limpiamos la clave
                txtJpassword.requestFocus();
            }
        }


    }//GEN-LAST:event_btnLoginBotonLoginActionPerformed

