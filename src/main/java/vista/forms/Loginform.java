    private void verificarConexionAlIniciar() {
        // 1. Estado inicial: Deshabilitamos el botón para evitar errores
        btnLogin.setEnabled(false);
        lblStatusDB.setText("Verificando servicios de Neon...");
        lblStatusDB.setForeground(Color.GRAY);

        // 2. Ejecutamos la prueba en un hilo para no congelar el Login
        new Thread(() -> {
            // Usamos el método que ya mejoramos en tu clase ConexionBD
            String status = ConexionBD.getServerInfo();

            // 3. Regresamos a la interfaz para mostrar el resultado
            java.awt.EventQueue.invokeLater(() -> {
                lblStatusDB.setText(status);

                if (status.contains("Conectado")) {
                    lblStatusDB.setForeground(new Color(40, 180, 99)); // Verde
                    btnLogin.setEnabled(true); // ¡Todo listo!
                } else {
                    lblStatusDB.setForeground(new Color(192, 57, 43)); // Rojo
                    btnLogin.setEnabled(true); // Dejamos que intente por si acaso
                    btnLogin.setText("Reintentar");
                }
            });
        }).start();
    }

